#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	char	*p;
	size_t	i;

	p = (char*)b;
	i = 0;
	while (i < n)
	{
		p[i] = (char)c;
		i++;
	}
	return ((void*)p);
}
